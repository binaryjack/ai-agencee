// ---------------------------------------------------------------------------
// Simulator scenario definitions — scripted event timelines for the browser
// showroom. Every delayMs is relative to the previous event (differential).
// The SSE API route streams these with proper timing.
// ---------------------------------------------------------------------------

export type SimEventKind =
  | 'dag:start'
  | 'lane:start'
  | 'lane:retry'
  | 'lane:escalate'
  | 'lane:handoff'
  | 'lane:human-review'
  | 'lane:abort'
  | 'lane:complete'
  | 'barrier:waiting'
  | 'barrier:released'
  | 'token:stream'
  | 'pii:scrubbed'
  | 'verdict:issued'
  | 'cost:update'
  | 'dag:complete'
  | 'dag:abort'

export interface ScriptedEvent {
  delayMs:      number
  type:         SimEventKind
  laneId?:      string
  content?:     string
  status?:      'pass' | 'escalated' | 'handed-off' | 'aborted'
  abortReason?: string
  costUsd?:     number
  totalUsd?:    number
  attempt?:     number
  reason?:      string
  targetLaneId?: string
  dagName?:     string
  laneIds?:     string[]
  barrierName?: string
  ready?:       number
  total?:       number
  pattern?:     string
  count?:       number
  durationMs?:  number
  verdict?:     string
}

export interface SimLane {
  id:         string
  label:      string
  icon:       string
  dependsOn:  string[]
}

export interface SimScenario {
  id:          string
  number:      string
  title:       string
  subtitle:    string
  description: string
  tags:        string[]
  accentColor: string   // Tailwind color name for gradient
  lanes:       SimLane[]
  events:      ScriptedEvent[]
}

